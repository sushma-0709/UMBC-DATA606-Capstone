    TRANSACTION_COUNT INTEGER,
    WITNESS_COUNT INTEGER,
    INPUT_COUNT INTEGER,
    OUTPUT_COUNT INTEGER,
    INPUT_TOTAL INTEGER,
    INPUT_TOTAL_USD INTEGER,
    OUTPUT_TOTAL INTEGER,
    OUTPUT_TOTAL_USD INTEGER,
    FEE_TOTAL INTEGER,
    FEE_TOTAL_USD FLOAT,
    FEE_PER_KB FLOAT,
    FEE_PER_KB_USD FLOAT,
    FEE_PER_KWU FLOAT,
    FEE_PER_KWU_USD FLOAT,
    CDD_TOTAL FLOAT,
    GENERATION INTEGER,
    GENERATION_USD FLOAT,
    REWARD INTEGER,
    REWARD_USD FLOAT,
    GUESSED_MINER VARCHAR(255)
);