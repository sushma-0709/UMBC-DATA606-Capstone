    ID INTEGER,
    HASH VARCHAR(255),
    TIME TIMESTAMP,
    MEDIAN_TIME TIMESTAMP,
    SIZE INTEGER,
    STRIPPED_SIZE INTEGER,
    WEIGHT INTEGER,
    VERSION INTEGER,
    VERSION_HEX INTEGER,
