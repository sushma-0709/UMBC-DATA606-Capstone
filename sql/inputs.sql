CREATE OR REPLACE TABLE inputs (
    BLOCK_ID INTEGER,
    TRANSACTION_HASH VARCHAR(255),
    INDEX INTEGER,
    TIME TIMESTAMP,
    VALUE INTEGER,
    VALUE_USD FLOAT,
    RECIPIENT VARCHAR(255),
    TYPE VARCHAR(255),
    SCRIPT_HEX VARCHAR(255),
    IS_FROM_COINBASE INTEGER,
    IS_SPENDABLE INTEGER,
    SPENDING_BLOCK_ID INTEGER,
    SPENDING_TRANSACTION_HASH VARCHAR(255),
    SPENDING_INDEX INTEGER,
    SPENDING_TIME TIMESTAMP,
    SPENDING_VALUE_USD FLOAT,
    SPENDING_SEQUENCE INTEGER,
    SPENDING_SIGNATURE_HEX VARCHAR(),
    SPENDING_WITNESS VARCHAR(),
    LIFESPAN INTEGER,
    CDD FLOAT
);